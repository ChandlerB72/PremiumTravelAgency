/**
 * iItineraryComponent is an interface
 * that ItineraryDecorator implements
 */
public interface iItineraryComponent {

    /**
     * Unimplemented method of getTrip()
     * @return trip to be used for itinerary
     */
    Trip getTrip();


    /**
     * Unimplemented method of output()
     * @return output string for itinerary
     */
    String output();
}
